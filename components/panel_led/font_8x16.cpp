// ============================================================
// CONVERSIÓN CHAR → ÍNDICE
// ============================================================
int get_char_index(char c) {
    if (c >= 'A' && c <= 'Z') return c - 'A';           // 0–25
    if (c >= 'a' && c <= 'z') return c - 'a';           // 0–25 (mismos glifos)

    if (c == ' ')  return 26;
    if (c == ',')  return 27;
    if (c == '.')  return 28;
    if (c == '!')  return 29;
    if (c == '?')  return 30;
    if (c == ':')  return 31;
    if (c == ';')  return 32;
    if (c == '-')  return 33;
    if (c == '(')  return 34;
    if (c == ')')  return 35;
    if (c == '/')  return 36;

    if (c >= '0' && c <= '9') return 37 + (c - '0');   // 0=37 … 9=46 ✅ completo

    return -1;  // No soportado
}
